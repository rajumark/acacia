package com.acacia.generator

import com.acacia.mapping.HybridNamingEngine
import com.acacia.model.ModifierFunction
import com.squareup.kotlinpoet.ClassName
import com.squareup.kotlinpoet.FileSpec
import com.squareup.kotlinpoet.FunSpec
import com.squareup.kotlinpoet.KModifier
import com.squareup.kotlinpoet.ParameterSpec
import com.squareup.kotlinpoet.TypeName
import com.squareup.kotlinpoet.asTypeName
import java.io.File

/**
 * Generates Kotlin code for short DSL functions using KotlinPoet for type-safety.
 * Uses HybridNamingEngine for intelligent naming (Golden list + Algorithmic collision-free).
 */
class KotlinGenerator {

    private val namingEngine = HybridNamingEngine()

    // Type references for Compose types
    private val modifierClassName = ClassName("androidx.compose.ui", "Modifier")
    private val dpClassName = ClassName("androidx.compose.ui.unit", "Dp")
    private val colorClassName = ClassName("androidx.compose.ui.graphics", "Color")
    private val shapeClassName = ClassName("androidx.compose.ui.graphics", "Shape")
    private val brushClassName = ClassName("androidx.compose.ui.graphics", "Brush")
    private val arrangementClassName = ClassName("androidx.compose.foundation.layout", "Arrangement")
    private val alignmentClassName = ClassName("androidx.compose.ui", "Alignment")
    private val borderStrokeClassName = ClassName("androidx.compose.foundation", "BorderStroke")
    private val dpOffsetClassName = ClassName("androidx.compose.ui.unit", "DpOffset")
    private val offsetClassName = ClassName("androidx.compose.ui.geometry", "Offset")
    private val textUnitClassName = ClassName("androidx.compose.ui.unit", "TextUnit")
    private val fontWeightClassName = ClassName("androidx.compose.ui.text.font", "FontWeight")
    private val textStyleClassName = ClassName("androidx.compose.ui.text", "TextStyle")
    private val imageVectorClassName = ClassName("androidx.compose.ui.graphics.vector", "ImageVector")
    private val painterClassName = ClassName("androidx.compose.ui.graphics.painter", "Painter")

    /**
     * Generates the ShortModifiers.kt file containing all short DSL functions.
     */
    /**
     * Generates the ShortModifiers.kt file containing all short DSL functions.
     * Uses HybridNamingEngine for intelligent naming (Golden list + Algorithmic).
     */
    fun generateShortModifiers(
        functions: List<ModifierFunction>,
        outputDir: File
    ): File {
        // Reset naming engine for fresh collision tracking
        namingEngine.reset()
        
        val fileSpec = buildFileSpec(functions)

        outputDir.mkdirs()
        val file = File(outputDir, "ShortModifiers.kt")
        file.writeText(fileSpec.toString())

        return file
    }

    private fun buildFileSpec(functions: List<ModifierFunction>): FileSpec {
        val fileBuilder = FileSpec.builder("com.acacia.generated", "ShortModifiers")
            .addFileComment("Generated by Acacia Shortify Plugin")
            .addFileComment("\nDO NOT EDIT - This file is generated automatically")
            .addFileComment("\nNaming: 50 Golden (handcoded) + Algorithmic (auto) with collision detection")

        // Add imports for commonly used types
        fileBuilder.addImport("androidx.compose.ui", "Modifier")
        fileBuilder.addImport("androidx.compose.ui.unit", "Dp")
        fileBuilder.addImport("androidx.compose.ui.graphics", "Color")
        fileBuilder.addImport("androidx.compose.foundation.layout", "padding")
        fileBuilder.addImport("androidx.compose.foundation.layout", "fillMaxWidth")
        fileBuilder.addImport("androidx.compose.foundation.layout", "fillMaxHeight")
        fileBuilder.addImport("androidx.compose.foundation.layout", "fillMaxSize")
        fileBuilder.addImport("androidx.compose.foundation.layout", "width")
        fileBuilder.addImport("androidx.compose.foundation.layout", "height")
        fileBuilder.addImport("androidx.compose.foundation.layout", "size")
        fileBuilder.addImport("androidx.compose.foundation", "background")
        // Only import functions we actually generate

        // Only generate safe, known-good functions to avoid conflicts and errors
        val safeFunctions = functions.filter { function ->
            // Only include functions we know work well and have safe parameter types
            val safeFunctionNames = setOf(
                "fillMaxWidth", "fillMaxHeight", "fillMaxSize", "background"
            )
            safeFunctionNames.contains(function.name) && 
            !function.name.contains("-") && 
            !function.name.contains("_") &&
            // Only include functions with safe parameter types
            function.parameters.all { param ->
                param.type in setOf("Float", "Brush", "Shape", "Color", "Dp")
            }
        }

        // Generate functions sorted by name for determinism
        safeFunctions.sortedBy { it.name }.forEach { function ->
            val shortName = namingEngine.generateShortName(function)
            val funSpec = buildFunctionSpec(function, shortName)
            fileBuilder.addFunction(funSpec)
        }

        return fileBuilder.build()
    }

    private fun buildFunctionSpec(function: ModifierFunction, shortName: String): FunSpec {
        val funBuilder = FunSpec.builder(shortName)
            .receiver(modifierClassName)
            .returns(modifierClassName)
            .addKdoc("Short DSL function for [Modifier.${function.name}]")

        // Add parameters
        function.parameters.forEach { param ->
            val paramSpec = buildParameterSpec(param)
            funBuilder.addParameter(paramSpec)
        }

        // Generate function body with proper parameter handling
        val body = generateFunctionBody(function)
        funBuilder.addStatement(body)

        return funBuilder.build()
    }

    private fun generateFunctionBody(function: ModifierFunction): String {
        // Check if function name contains special characters or looks like an internal function
        if (function.name.contains("-") || function.name.contains("_") || 
            function.name.matches(Regex(".*[A-Z][a-z0-9]{6,}.*"))) {
            // Skip generating functions for internal/mangled functions - return identity
            return "return this"
        }

        return when (function.name) {
            // Core layout functions that definitely exist
            "fillMaxWidth" -> {
                if (function.parameters.isNotEmpty()) {
                    "return this.fillMaxWidth(${function.parameters.first().name})"
                } else {
                    "return this.fillMaxWidth()"
                }
            }
            "fillMaxHeight" -> {
                if (function.parameters.isNotEmpty()) {
                    "return this.fillMaxHeight(${function.parameters.first().name})"
                } else {
                    "return this.fillMaxHeight()"
                }
            }
            "fillMaxSize" -> {
                if (function.parameters.isNotEmpty()) {
                    "return this.fillMaxSize(${function.parameters.first().name})"
                } else {
                    "return this.fillMaxSize()"
                }
            }
            "background" -> {
                val params = function.parameters
                when (params.size) {
                    1 -> "return this.background(${params[0].name})"
                    2 -> "return this.background(${params[0].name}, ${params[1].name})"
                    else -> {
                        // Handle complex background with color, shape, etc.
                        val colorParam = params.find { it.type.contains("Color") || it.type.contains("Brush") }
                        val shapeParam = params.find { it.type.contains("Shape") }
                        when {
                            colorParam != null && shapeParam != null -> 
                                "return this.background(${colorParam.name}, ${shapeParam.name})"
                            colorParam != null -> 
                                "return this.background(${colorParam.name})"
                            else -> {
                                val paramNames = params.joinToString(", ") { it.name }
                                "return this.background($paramNames)"
                            }
                        }
                    }
                }
            }
            "padding" -> {
                val params = function.parameters
                when (params.size) {
                    1 -> {
                        if (params[0].type == "Dp" || params[0].type == "PaddingValues") {
                            "return this.padding(${params[0].name})"
                        } else {
                            "return this" // Skip unknown parameter types
                        }
                    }
                    2 -> {
                        if (params.all { it.type == "Dp" }) {
                            "return this.padding(horizontal = ${params[0].name}, vertical = ${params[1].name})"
                        } else {
                            "return this"
                        }
                    }
                    4 -> {
                        if (params.all { it.type == "Dp" }) {
                            "return this.padding(start = ${params[0].name}, top = ${params[1].name}, end = ${params[2].name}, bottom = ${params[3].name})"
                        } else {
                            "return this"
                        }
                    }
                    else -> "return this"
                }
            }
            "width" -> {
                if (function.parameters.isNotEmpty()) {
                    val param = function.parameters.first()
                    when (param.type) {
                        "Dp" -> "return this.width(${param.name})"
                        "IntrinsicSize" -> "return this.width(${param.name})"
                        else -> "return this" // Skip unknown parameter types
                    }
                } else {
                    "return this.width()"
                }
            }
            "height" -> {
                if (function.parameters.isNotEmpty()) {
                    val param = function.parameters.first()
                    when (param.type) {
                        "Dp" -> "return this.height(${param.name})"
                        "IntrinsicSize" -> "return this.height(${param.name})"
                        else -> "return this" // Skip unknown parameter types
                    }
                } else {
                    "return this.height()"
                }
            }
            // Common modifier functions that should exist
            "size", "clickable", "offset", "alpha", "rotate", "scale", "zIndex" -> {
                val paramNames = function.parameters.joinToString(", ") { it.name }
                "return this.${function.name}($paramNames)"
            }
            else -> {
                // For unknown functions, be defensive and return identity
                "return this"
            }
        }
    }

    private fun buildParameterSpec(param: ModifierFunction.Parameter): ParameterSpec {
        val typeName = resolveTypeName(param.type)
        val paramBuilder = ParameterSpec.builder(param.name, typeName)

        // Add default value if present
        if (param.hasDefault && param.defaultValue != null) {
            paramBuilder.defaultValue(param.defaultValue)
        }

        return paramBuilder.build()
    }

    /**
     * Resolves a type string to a KotlinPoet TypeName.
     */
    private fun resolveTypeName(type: String): TypeName {
        return when (type) {
            "Dp" -> dpClassName
            "Color" -> colorClassName
            "Shape" -> shapeClassName
            "Brush" -> brushClassName
            "Arrangement" -> arrangementClassName
            "Alignment" -> alignmentClassName
            "BorderStroke" -> borderStrokeClassName
            "DpOffset" -> dpOffsetClassName
            "Offset" -> offsetClassName
            "Float" -> Float::class.asTypeName()
            "Int" -> Int::class.asTypeName()
            "Double" -> Double::class.asTypeName()
            "Boolean" -> Boolean::class.asTypeName()
            "String" -> String::class.asTypeName()
            "TextUnit" -> textUnitClassName
            "FontWeight" -> fontWeightClassName
            "TextStyle" -> textStyleClassName
            "ImageVector" -> imageVectorClassName
            "Painter" -> painterClassName
            else -> {
                // For unknown types, use dynamic ClassName if it looks like a class reference
                if (type.contains(".")) {
                    val parts = type.split(".")
                    val packageName = parts.dropLast(1).joinToString(".")
                    val className = parts.last()
                    ClassName(packageName, className)
                } else {
                    // Fallback to Any for truly unknown types
                    ClassName("kotlin", "Any")
                }
            }
        }
    }

    /**
     * Returns naming statistics for debugging/diagnostics.
     */
    fun getNamingStatistics(): HybridNamingEngine.NamingStatistics {
        return namingEngine.getStatistics()
    }
}
